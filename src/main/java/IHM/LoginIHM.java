    // ➜ Méthode pour gérer la connexion
     private void loginAction() throws RemoteException {
        String email = txtEmail.getText().trim();
        String password = txtPassword.getText().trim(); // Récupérer correctement le mot de passe

        // Vérification des champs vides
        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Appeler la méthode login du client RMI
        boolean admin = client.login(email, password);

        // Si la connexion est réussie, ouvrir la page EtdManager
        if (admin) {
            JOptionPane.showMessageDialog(this, "Connexion réussie !");
            this.dispose(); // Fermer la fenêtre de connexion
            new AdminAccueilIHM().setVisible(true); // Ouvrir la page EtdManager
        } else {
            JOptionPane.showMessageDialog(this, "Email ou mot de passe incorrect !", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
